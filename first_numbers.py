# using the range() function; colon (:) is very important; don't forget it otherwise you cannot get the result you wnat. 
for number in range(1,5):
	print(number)

for number in range(1,6):
	print(number)

for number in range(6):
	print(number)

# using range() to make a list of numbers
numbers = list(range(1,6))
print(numbers)
